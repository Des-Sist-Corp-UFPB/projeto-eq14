CREATE TABLE enderecos (
    id               UUID         PRIMARY KEY DEFAULT gen_random_uuid(),
    usuario          UUID         NOT NULL UNIQUE REFERENCES usuarios (id) ON DELETE CASCADE,
    municipio        UUID         REFERENCES municipios (id),
    bairro           VARCHAR(120),
    logradouro       VARCHAR(180),
    numero           VARCHAR(20),
    complemento      VARCHAR(120),
    ponto_referencia VARCHAR(180),
    cep              VARCHAR(9),
    atualizado_em    TIMESTAMPTZ  NOT NULL DEFAULT now()
);

CREATE INDEX idx_enderecos_municipio ON enderecos (municipio);
CREATE INDEX idx_enderecos_bairro    ON enderecos (bairro);

COMMENT ON TABLE enderecos IS 'Endereco estruturado do passageiro (1-para-1); base de filtros/analise';

